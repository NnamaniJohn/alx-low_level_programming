#include "3-calc.h"

/**
 * op_add - function to add
 * @a: int
 * @b: int
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to substract
 * @a: int
 * @b: int
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multipy
 * @a: int
 * @b: int
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - funtion to divide
 * @a: int
 * @b: int
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to find mod
 * @a: int
 * @b: int
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
